package PackageLab;

public class PrintAsciiValue {

	public static void main(String[] args) {
		
		char ch = 'A'; 
		int ascii = ch; 
		
		int castAscii = (int) ch; 
		System.out.println("The ASCII valuee of " + ch + "is :" + ascii);
		System.out.println("The ASCII value of " + ch + " is: " + castAscii);
	
	}

}
