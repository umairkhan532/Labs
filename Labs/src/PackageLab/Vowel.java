package PackageLab;

public class Vowel {

	public static void main(String[] args) {
		
		char ch = 'i'; 
		
		if (ch == 'a' || ch == 'i' || ch =='o' || ch == 'u' ) {
			System.out.println( ch + "   : is a vowel");
		}
		else {
			System.out.println(ch + "    : is a consonant");
		}
		
		
	}

}
